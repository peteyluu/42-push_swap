#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char *pd;

	pd = dst;
	while (len--)
		if (*src)
			*pd++ = *src++;
		else
			*pd++ = '\0';
	return (dst);
}
