#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*res;
	char	*pres;

	if (!s1 || !s2)
		return (NULL);
	if (!(res = ft_strnew(ft_strlen(s1) + ft_strlen(s2))))
		return (NULL);
	pres = res;
	while (*s1)
		*pres++ = *s1++;
	while (*s2)
		*pres++ = *s2++;
	*pres = '\0';
	return (res);
}
